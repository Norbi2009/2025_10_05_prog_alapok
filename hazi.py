# Téglalap adatok
a = 5   # egyik oldal hossza
b = 8   # másik oldal hossza

print("Téglalap oldalai:", a, "és", b)

# Kerület: K = 2 * (a + b)
K = 2 * (a + b)
print("Téglalap kerülete:", K)

# Terület: T = a * b
T = a * b
print("Téglalap területe:", T)

# Kör adatok
r = 4   # sugár alapérték
print("Kör sugara:", r)

# Átmérő: d = 2 * r
d = 2 * r
print("Kör átmérője:", d)

# Kerület: C = 2 * pi * r
pi = 3.14
C = 2 * pi * r
print("Kör kerülete:", C)

# Terület: A = pi * r^2
A = pi * r * r
print("Kör területe:", A)
